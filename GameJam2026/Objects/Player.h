#pragma once

//----------------------------------------
// Player
// ・プレイヤーの成績管理クラス
// ・正解数とスコアを保持する
//----------------------------------------
class Player
{
public:
    Player();

    //----------------------------------------
    // 正解数を 1 増やす
    //----------------------------------------
    void AddCorrent();      // １問正解

    //----------------------------------------
    // スコアを加算する
    //----------------------------------------
    void AddScore(int score);   // 正答数

    //----------------------------------------
    // 成績をリセット（正解数・スコアを 0 に戻す）
    //----------------------------------------
    void Reset();

    //----------------------------------------
    // 正解数の取得
    //----------------------------------------
    int GetTotalCorrect() const;

    //----------------------------------------
    // スコアの取得
    //----------------------------------------
    int GetTotalScore() const;

private:
    int totalCorrect_;   // 正解数
    int totalScore_;     // スコア
};